var spiralize = function(size) {
  
  var spiral = [];
  for (var i = 0; i < size; i++) {
    spiral.push([]);
    for (var j = 0; j < size; j++) {
      spiral[i].push(0);
    }
  }
  var x = 0;
  var y = 1;
  var complete = false;

  while (!complete) {
    spiral[y][x] = '.';
    if (spiral[y][x + 2] !== undefined && spiral[y][x + 2] !== '.') {
      x++;
    } else if (spiral[y + 2][x] !== undefined && spiral[y + 2][x] !== '.') {
      y++;
    } else if (spiral[y][x - 2] !== undefined && spiral[y][x - 2] !== '.') {
      x--;
    } else if (spiral[y - 2][x] !== undefined && spiral[y - 2][x] !== '.') {
      y--;
    } else {
      complete = true;
    }
  }
}


